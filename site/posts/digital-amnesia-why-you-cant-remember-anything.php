<h2>The Google Effect</h2>

<p>In 2011, a team at Columbia University led by psychologist Betsy Sparrow published a landmark study in <em>Science</em>. They found that when people know information is available online, they\'re significantly less likely to remember the information itself &mdash; but more likely to remember <em>where to find it</em>.</p>

<p>Your brain hasn\'t gotten worse. It\'s gotten efficient. It\'s treating Google like an external hard drive and optimizing its own storage accordingly. Why memorize a fact when you can memorize a search query?</p>

<p>This is called <strong>cognitive offloading</strong> &mdash; the tendency to rely on external tools (phones, search engines, GPS) for information you would otherwise store in your brain.</p>

