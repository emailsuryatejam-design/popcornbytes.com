<h2>The Dial-Up Era</h2>

<p>First, the context: most homes in the late 1990s connected to the internet via dial-up. That meant a maximum speed of 56 kbps &mdash; kilobits per second. For comparison, a basic broadband connection today is roughly 1,000 times faster.</p>

<p>At 56 kbps, a typical 4-minute MP3 file (about 4 MB) took around 12 to 15 minutes to download. That\'s if everything went perfectly. If someone in your house picked up the phone, the connection dropped, and you started over.</p>

<h2>Napster Changed Everything (Slowly)</h2>

<p>When Napster launched in 1999, it felt like magic. For the first time, you could search for almost any song and download it directly from another person\'s computer. The concept was revolutionary. The execution was... patience-testing.</p>

<p>Here\'s what a typical Napster session looked like:</p>

<ol>
<li>Search for the song you want.</li>
<li>Find 15 results, most of which are mislabeled. "Smells Like Teen Spirit" might actually be a Weird Al parody, a live bootleg, or a completely different song by a completely different artist.</li>
<li>Pick one and start downloading. Watch the progress bar inch forward.</li>
<li>At 85% complete, someone picks up the phone. Connection lost. Start over.</li>
<li>Try again. This time, leave the computer running overnight.</li>
