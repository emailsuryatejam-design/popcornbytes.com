</ol>

<h2>The Joy of Success</h2>

<p>But when it worked? When you actually got the song you wanted? The feeling was incredible. You\'d built up anticipation for hours. You\'d invested real time and effort. And now this song was <em>yours</em> &mdash; saved on your hard drive, ready to play whenever you wanted.</p>

<p>There was a sense of accomplishment that streaming can never replicate. You didn\'t just listen to a song. You <em>acquired</em> it.</p>

<h2>The LimeWire Roulette</h2>

<p>After Napster was shut down in 2001, LimeWire and Kazaa took over. They were worse in almost every way. Download speeds were slower. Mislabeled files were more common. And the risk of accidentally downloading something you definitely didn\'t want was very real.</p>

<p>LimeWire was essentially Russian roulette for your computer. For every successfully downloaded song, you probably picked up three viruses and a toolbar you\'d never get rid of.</p>

<h2>What We Had</h2>

<p>Looking back, the dial-up download era had something modern streaming lacks: <strong>investment</strong>. When a song took effort to acquire, you listened to it more carefully. You appreciated it more deeply. You didn\'t skip it after 10 seconds.</p>

<p>Today, we have access to all the music in the world, and we listen to it like it\'s wallpaper. Maybe there was something to be said for a world where getting one song was an adventure.</p>
