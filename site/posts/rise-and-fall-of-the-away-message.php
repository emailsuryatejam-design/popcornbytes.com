<h2>What Was an Away Message?</h2>

<p>For the uninitiated: AIM (AOL Instant Messenger) let you set a short message that displayed when you were away from your computer. In theory, it told people you were unavailable. "At dinner" or "Doing homework" would have been perfectly reasonable.</p>

<p>But that\'s not what we did.</p>

<p>Instead, the away message became the first real form of social media. Before Facebook statuses, before tweets, before Instagram captions, there was this tiny text box where you could broadcast your mood, your personality, and your deepest teenage feelings to everyone on your buddy list.</p>

