<p>Social media platforms are engineered around variable reward schedules &mdash; the same mechanism that makes slot machines addictive. You scroll past boring content, boring content, boring content, and then &mdash; something interesting. A hit. Your brain releases dopamine, not from the content itself, but from the <em>unpredictability</em> of finding it.</p>

<p>This is why you keep scrolling even when most of what you see is junk. Your brain is chasing the next random reward, just like a gambler chasing the next win.</p>

