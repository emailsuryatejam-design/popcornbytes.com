<li><strong>Social comparison spiraling.</strong> Instagram and TikTok create what psychologists call "upward social comparison" &mdash; you\'re constantly measuring yourself against curated highlights of other people\'s lives. The result is a persistent, low-grade sense of inadequacy.</li>
</ul>

