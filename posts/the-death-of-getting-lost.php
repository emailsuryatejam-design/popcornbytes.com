<h2>Before the Blue Dot</h2>

<p>There was an era &mdash; and it wasn\'t that long ago &mdash; when leaving your house meant accepting a small degree of uncertainty. You had a general sense of where you were going, maybe a hand-drawn map from a friend, and a healthy dose of hope.</p>

